			<h2>World Bank sees worse contraction for PH economy due to virus, typhoons</h2>
			<p class="date">Posted at Dec 08 2020 07:21 PM</p>
			<span>

				<p>MANILA – The World Bank further downgraded its 2020 economic outlook for the Philippines following the worse-than-expected contraction in the third quarter as well as the typhoons that ravaged the country in October and November.</p>

			<p>The multilateral lender said, the Philippine economy will likely shrink by 8.1 percent this year, which is worse than its already downgraded forecast of 6.9 percent issued in October.</p>

			<p>However, it sees the economy recovering by 5.9 percent in 2021 and 6 percent in 2022. Though this is still below the government's expectations of a 6.5 to 7.5 percent growth in 2021 and 8 percent to 10 percent in 2022.</p>

			<p>Among the growth drivers, it cited for the years ahead are the spending related to the 2022 elections as well as the string of infrastructure projects being pushed by the government, which the World Bank said, would help create jobs and ensure a stable economic footing.</p>

			<p>But the World Bank also warned the recession this year will temporarily reverse the gains made in poverty reduction.</p>

			<p>“The series of natural disasters that hit the country while we are battling the pandemic highlights the importance of mainstreaming disaster risk reduction and climate change adaptation into policy and planning,” said Ndiame Diop, World Bank Country Director for Brunei, Malaysia, Thailand and the Philippines. </p>

			<p>“While the Philippines is financially resilient, stronger coordination, execution and implementation will help further improve social and physical resilience to frequent shocks.”</p>

			<p>Typhoons Rolly (international name Goni), Siony (Atsani), and Ulysses (Vamco) that hit the country in November in just a span of two weeks have caused billions of pesos in damage to infrastructure and agriculture.</p>

			<p>Prior to these disaster events, the economy had already posted a nine-month 10 percent contraction, the worst since the 1985 debt crisis, due to a plunge in private domestic demand, deep contraction in investment activities, and weak exports. </p>

			<p>Private consumption, which accounts for two-thirds of the Philippine economy also declined at a record pace because of high unemployment and falling incomes.</p>

			<p>Economic managers see GDP shrinking up to 9.5 percent this year</p>

			<p>“While addressing the pandemic, the country needs to sustain focus on the structural reform agenda,” said Rong Qian, World Bank Senior Economist. </p>

			<p>“Speeding up reforms that improve the business environment, foster competition, and strengthen resilience against natural disasters will support the economic recovery and boost productivity growth in the long term.”</p>

			<p>She added, government's fiscal response under the Bayanihan to Heal as One Act (Bayanihan 1) and the Bayanihan to Recover as One Act (Bayanihan 2) can help boost the country's growth potential.</p>

			<p>"As we have seen, after Bayanihan 1, Bayanihan 2 has put together a second package and this has been more surgical than the first package which is needed to complement what has been approved in Bayanihan 1 which is focused on the poor and vulnerable response," Qian explained.</p>

			<p>Gov’t says fiscal stimulus enough despite forecasting worst post-war recession</p>

			<p>Likewise, she said, an early rollout of a vaccine against COVID-19 can help boost the Philippines' economic prospects.</p>

			<p>"In our baseline assumption, we don't assume that the vaccine will be rolled out very soon. Therefore, if the vaccine gets rolled out next year, it will really be an upside risk to our baseline projection," Qian said.</p>

			<p>She explained the vaccine can help bring in more jobs as more people are able to re-enter the economy.</p>

			<p>Qian added this can also help build confidence not just in the Philippines but also in other countries which would improve the prospects for international trade.</p>

			<p>Meanwhile, Diop said, the World Bank stands ready to support the Philippines in recovering from the health crisis.</p>

			<p>"Our agreement is to focus in three areas. One is to invest in the Filipinos, this is really one of the key strategies focus of the World Bank and that's supporting health, supporting education, supporting social protection and social assistance and supporting communities," he said.</p>

			<p>The World Bank Group is preparing up to $160 billion over a 15-month period ending June 2021 to help more than 100 countries protect the poor and vulnerable, support businesses, and bolster economic recovery from the negative impact of the COVID-19 pandemic.</p></span>


